/*
//assignment operators\\
(a += b) --> (a = a + b)
(a -= b) --> (a = a - b)
(a *= b) --> (a = a * b)
(a /= b) --> (a = a / b)
(a %= b) --> (a = a % b)
 */
fun main(){
    var num = 5
    num += 5 //num = num + 5
    println(num)
    ////////////////////////////////////////////

    var num1 = 5
    num1 -= 2 //num1 = num1 - 2
    println(num1)

    ////////////////////////////////////////////

    var num2 = 5
    num2 *= 5 //num2 = num2 * 5
    println(num2)

    ////////////////////////////////////////////

    var num3 = 5
    num3 /= 5 //num3 = num3 / 5
    println(num3)

    ////////////////////////////////////////////

    var num4 = 5
    num4 %= 5 //num4 = num4 % 5
    println(num4)

    ////////////////////////////////////////////
}