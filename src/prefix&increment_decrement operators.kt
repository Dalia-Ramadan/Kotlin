package operators
/*
unary plus --> (+a)
unary minus --> (-a)
not --> (!a)
increment --> (++a)
decrement --> (--a)
 */
fun main(){
    val a = 5
    var res = +a
    println(res)
    ////////////////////////////////////
    res = -a
    println(res)
    ///////////////////////////////////
    res = !a
    println(res)
}